use strict;
use warnings 'FATAL' => 'all';

my %h2m;
open(my $fh, "mouse2human.tsv") or die;
while (<$fh>) {
	chomp;
	my ($mouse, $human) = split;
	next unless defined $human;
	$h2m{$human} = $mouse;
}

my %m2s;
open(my $fh2, "mm_genes_strains.tsv") or die;
while (<$fh2>) {
	chomp;
	my ($gene, $mmrrcid) = split;
	push @{$m2s{$gene}}, $mmrrcid;
}

my %hphe;
open(my $fh3, "hsphenotypes.tsv") or die;
while (<$fh3>) {
	chomp;
	my ($gene, $phe) = split('\s+', $_, 2);
	$hphe{$gene} = $phe;
}

my $search;
my %keep;
while (<DATA>) {
	if (/^OMIM.+'"(.+)"/) {
		$search = $1;
		next;
	}
	next unless /^[%#\*]?\d+/;
	my ($omim, $title, $inct, $cyto, $coord, $entrz) = split(/\t/);
	($omim) = $omim =~ /(\d+)/;
	next unless $title =~ /(.+); (\w+)$/;
	my $desc = $1;
	my $gene = $2;
	next unless defined $h2m{$gene};
	next unless defined $hphe{$gene};
	
	if (not defined $keep{$gene}) {
		$keep{$gene} = {
			'omim' => $omim,
			'desc' => $desc,
			'mouse' => $h2m{$gene},
			'phen' => $hphe{$gene},
		};
	}
	push @{$keep{$gene}{'search'}}, $search;
}




print "# MMRRCID, Human, Mouse, OMIM, Description, Phenotype\n";
my %seen;
foreach my $gene (sort keys %keep) {
	next unless defined $m2s{$keep{$gene}{'mouse'}};
	my @strain = @{$m2s{$keep{$gene}{'mouse'}}};
	#next if @strain > 10; # EMS stuff... 
	#print scalar(@strain), " strains with this gene\n";
	foreach my $strain (@strain) {
		my $out = join("\t",
			$strain,
			$gene,
			$keep{$gene}{'mouse'},
			$keep{$gene}{'omim'},
			$keep{$gene}{'desc'},
			$keep{$gene}{'phen'},
			#join(", ", @{$keep{$gene}{'search'}})
			) . "\n";
		print $out unless $seen{$strain} or $seen{$gene};
		$seen{$strain} = 1;
		$seen{$gene} = 1;
	}
}

